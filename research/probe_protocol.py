"""Systematically map vendor HID protocol for Acer PopGo (32C2:0066)."""
from __future__ import annotations

import time
from collections import defaultdict

import hid

VID = 0x32C2
PID = 0x0066


def open_vendor():
    for d in hid.enumerate(VID, PID):
        if d["usage_page"] == 0xFFB5:
            h = hid.device()
            h.open_path(d["path"])
            h.set_nonblocking(True)
            return h
    raise RuntimeError("Vendor interface 0xFFB5 not found")


def drain(h, timeout=0.05):
    """Read all pending input until quiet."""
    results = []
    t0 = time.time()
    while time.time() - t0 < timeout:
        r = h.read(64)
        if r:
            results.append(list(r))
            t0 = time.time()  # extend while data flows
        else:
            time.sleep(0.005)
    return results


def send(h, packet, wait=0.08):
    """Write 8-byte packet and collect responses."""
    data = bytes(packet[:8] + [0] * max(0, 8 - len(packet)))
    # drain first
    drain(h, 0.02)
    try:
        h.write(data)
    except Exception as e:
        return [], str(e)
    time.sleep(wait)
    return drain(h, wait), None


